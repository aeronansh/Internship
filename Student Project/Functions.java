package day5;


public class Functions {
	
	
	
	public static int totalMarks ( int x , int y , int z , int p , int q ) {
		
		int total = x + y + z + p + q;
		return total ;
	}
	
	public static float percentageStudent ( float x ) {
		
		float per = ( x * 100 ) / 500 ;
		return per ;
	}
	
	
	

}
